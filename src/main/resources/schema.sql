CREATE TABLE IF NOT EXISTS Messages
(
    message_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name      VARCHAR
);

CREATE TABLE IF NOT EXISTS BotMessageTexts
(
    text_ai_id INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    message_id  INTEGER,
    bot_text    TEXT,
    FOREIGN KEY (message_id) REFERENCES Messages(message_id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS MessageTexts
(
    text_id    INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    message_id INTEGER,
    text       TEXT,
    FOREIGN KEY (message_id) REFERENCES Messages(message_id) ON DELETE CASCADE
);
